#include <iostream>

int main()
{
    std::string myString = "Hello World!";    // string is an array of characters
    int myInt = 15;                           // if input is a decimal, it will be rounded down
    const int myConstInt = 15;                // constant integer (cannot be changed)
    float myFloat = 3.14;                     // 6 decimal places
    double myDouble = 3.14159265358979323846; // 15 decimal places
    bool myBool = true;                       // true or false
    char myChar = 'a';                        // single quotes for char, double quotes for string

    return 0;
}